('10.0.0.22', '1.1.1.1', 2048, '2026-05-17 14:15:00');          -- Обычный запрос (2 КБ)

-- 3. Решение: SQL-запрос для SOC-аналитика
-- Ищем все внутренние IP-адреса, которые отправили больше 1 Гигабайта (1073741824 байт) данных за одну сессию.
-- Заодно переводим байты в мегабайты для красивого вывода.
SELECT src_ip, dst_ip, 
       ROUND(bytes_sent / 1024.0 / 1024.0, 2) AS megabytes_sent, 
       timestamp 
FROM firewall_traffic 
WHERE bytes_sent > 1073741824
ORDER BY bytes_sent DESC;
