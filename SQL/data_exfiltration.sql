-- Задача: Выявить потенциальную утечку данных (Data Exfiltration)
-- Мы анализируем логи межсетевого экрана (Firewall) на предмет аномального объема исходящего трафика.

-- 1. Создаем таблицу логов трафика (в PostgreSQL используется тип SERIAL для автоинкремента)
CREATE TABLE IF NOT EXISTS firewall_traffic (
    id SERIAL PRIMARY KEY,
    src_ip VARCHAR(15),       -- Внутренний IP-адрес источника (сотрудник)
    dst_ip VARCHAR(15),       -- Внешний IP-адрес назначения (куда идут данные)
    bytes_sent BIGINT,        -- Количество отправленных байт
    timestamp TIMESTAMP
);

-- 2. Заполняем тестовыми данными (в реальной жизни тут миллионы строк)
