"""
Инструкция: Простой генератор случайных MAC-адресов.
Иногда в сетях (или при пентесте) нужно временно подменить свой MAC-адрес (MAC Spoofing),
чтобы обойти фильтры на роутерах, бесплатном Wi-Fi или скрыть свое устройство.
Этот скрипт генерирует локальный (unicast) MAC-адрес случайным образом.
"""
import random

def generate_random_mac():
    # Первый байт (02) означает, что адрес задан локально (Locally Administered) 
    # и является Unicast (индивидуальным). Это правило стандарта Ethernet.
    mac = [0x02] 
    
    # Генерируем оставшиеся 5 байт (от 0 до 255) случайным образом
    for _ in range(5):
        mac.append(random.randint(0x00, 0xff))
        
    # Форматируем в привычный вид: 02:XX:XX:XX:XX:XX
    return ':'.join(f'{b:02x}' for b in mac)

if __name__ == "__main__":
    new_mac = generate_random_mac()
    print(f"Сгенерированный MAC-адрес для подмены: {new_mac}")
